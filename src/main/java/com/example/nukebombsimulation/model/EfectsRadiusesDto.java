package com.example.nukebombsimulation.model;

public class EfectsRadiusesDto {
    private double fireballRadius;
    private double heavyBlastDamageRadius;
    private double radiationRadius;
    private double averageBlastDamageRadius;
    private double thermalRadiationRadius;
    private double lightBlastDamageRadius;

    public double getFireballRadius() {
        return fireballRadius;
    }

    public void setFireballRadius(double fireballRadius) {
        this.fireballRadius = fireballRadius;
    }

    public double getHeavyBlastDamageRadius() {
        return heavyBlastDamageRadius;
    }

    public void setHeavyBlastDamageRadius(double heavyBlastDamageRadius) {
        this.heavyBlastDamageRadius = heavyBlastDamageRadius;
    }

    public double getRadiationRadius() {
        return radiationRadius;
    }

    public void setRadiationRadius(double radiationRadius) {
        this.radiationRadius = radiationRadius;
    }

    public double getAverageBlastDamageRadius() {
        return averageBlastDamageRadius;
    }

    public void setAverageBlastDamageRadius(double averageBlastDamageRadius) {
        this.averageBlastDamageRadius = averageBlastDamageRadius;
    }

    public double getThermalRadiationRadius() {
        return thermalRadiationRadius;
    }

    public void setThermalRadiationRadius(double thermalRadiationRadius) {
        this.thermalRadiationRadius = thermalRadiationRadius;
    }

    public double getLightBlastDamageRadius() {
        return lightBlastDamageRadius;
    }

    public void setLightBlastDamageRadius(double lightBlastDamageRadius) {
        this.lightBlastDamageRadius = lightBlastDamageRadius;
    }
}
